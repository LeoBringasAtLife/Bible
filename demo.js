// Función para obtener parámetros de la URL
function getQueryParam(param) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(param);
}

// Función para cargar el devocional
function loadDevotional() {
    const devotionalId = parseInt(getQueryParam('id'));
    const devotionalContainer = document.getElementById('devotional-content');

    if (!devotionalId) {
        devotionalContainer.innerHTML = `
                    <div class="error-message">
                        <h2>Devocional no especificado</h2>
                        <p>No se ha proporcionado un ID de devocional válido.</p>
                        <a href="../index.html" class="nav-button">
                            <i class="fas fa-home"></i> Volver al Inicio
                        </a>
                    </div>
                `;
        return;
    }

    const devotional = devotionals.find(d => d.id === devotionalId);

    if (!devotional) {
        devotionalContainer.innerHTML = `
                    <div class="error-message">
                        <h2>Devocional no encontrado</h2>
                        <p>El devocional con ID ${devotionalId} no existe.</p>
                        <a href="index.html" class="nav-button">
                            <i class="fas fa-home"></i> Volver al Inicio
                        </a>
                    </div>
                `;
        return;
    }

    // Calcular devocionales anterior y siguiente
    const prevDevotional = devotionals.find(d => d.id === devotionalId - 1);
    const nextDevotional = devotionals.find(d => d.id === devotionalId + 1);

    devotionalContainer.innerHTML = `
                <div class="devotional-card">
                    <div class="devotional-header">
                        <h1 class="devotional-title">${devotional.title}</h1>
                        <div class="devotional-meta">
                            <span>${devotional.author}</span>
                            <span>${devotional.date}</span>
                        </div>
                    </div>
                    
                    <div class="devotional-content">
                        <img src="${devotional.image}" alt="${devotional.title}" class="devotional-image">
                        <div class="devotional-text">
                            ${devotional.content}
                        </div>
                    </div>
                    
                    <div class="devotional-navigation">
                        ${prevDevotional ? `
                            <a href="devo.html?id=${prevDevotional.id}" class="nav-button">
                                <i class="fas fa-arrow-left"></i> Anterior
                            </a>
                        ` : '<div></div>'}
                        
                        ${nextDevotional ? `
                            <a href="devo.html?id=${nextDevotional.id}" class="nav-button">
                                Siguiente <i class="fas fa-arrow-right"></i>
                            </a>
                        ` : ''}
                    </div>
                </div>
            `;

    // Actualizar el título de la página
    document.title = `${devotional.title} - Lectura Diaria`;
}

// Cargar el devocional cuando la página esté lista
document.addEventListener('DOMContentLoaded', loadDevotional);